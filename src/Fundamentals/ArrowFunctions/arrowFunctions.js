// Single Line Arrow Funcitons
console.log("Single Line Arrow Functions")
let sum = (a,b) => a+b
console.log(sum(2,3))

// MultiLine Arrow Functions
console.log("Multiline Arrow FUnctions")
let multiply = (a,b) => {
    console.log("Inside MultiLine FUnciton")
    return a * b
}
console.log(multiply(4,5))